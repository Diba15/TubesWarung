    private void buttonCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCariActionPerformed
        String cariNama = fieldCariBarang.getText();
        if (cariNama == null) {
            JOptionPane.showMessageDialog(rootPane, "Masukkan nama Barang dengan benar!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            df.setRowCount(0);
            resetSearchButton.setVisible(true);
            try {
                ResultSet rs = op.readData("SELECT * FROM barang WHERE namaBarang = '" + cariNama + "'");
                if (rs.next()) {
                    String id = rs.getString("id_barang");
                    String namaBarang = rs.getString("namaBarang");
                    String jenisBarang = rs.getString("jenisBarang");
                    String rasa = rs.getString("rasa");
                    String jenis = rs.getString("jenis");
                    int harga = rs.getInt("harga");
                    int qty = rs.getInt("kuantitas");
                    df.addRow(new Object[]{1, namaBarang, jenisBarang, harga, qty});
                    fieldCariBarang.setText(null);
                }
            } catch (SQLException ex) {
                Logger.getLogger(HalPelanggan.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_buttonCariActionPerformed

    private void resetSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetSearchButtonActionPerformed
        df.setRowCount(0);
        war.resetList();
        resetSearchButton.setVisible(false);
        try {
            ResultSet rs = op.readData("SELECT * FROM barang");
            int i = 1;
            while (rs.next()) {
                String namaBarang = rs.getString("namaBarang");
                String jenisBarang = rs.getString("jenisBarang");
                String jenis = rs.getString("jenis");
                String idBarang = rs.getString("id_barang");
                String rasa = rs.getString("rasa");
                int hargaBarang = rs.getInt("Harga");
                int qty = rs.getInt("kuantitas");
                if (jenisBarang.equalsIgnoreCase("Etc")) {
                    war.addBarangLain(jenis, idBarang, namaBarang, jenisBarang, qty, hargaBarang);
                } else {
                    war.addMakanMinum(rasa, jenis, idBarang, namaBarang, jenisBarang, qty, hargaBarang);
                }
                df.addRow(new Object[]{i, namaBarang, jenisBarang, hargaBarang, qty});
                i++;
            }

        } catch (SQLException ex) {
            Logger.getLogger(HalPelanggan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_resetSearchButtonActionPerformed

