    public final void displayBarang() {
        try {
            ResultSet rs = op.readData("SELECT * FROM barang");
            int i = 1;
            while (rs.next()) {
                String namaBarang = rs.getString("namaBarang");
                String jenisBarang = rs.getString("jenisBarang");
                String jenis = rs.getString("jenis");
                String idBarang = rs.getString("id_barang");
                String rasa = rs.getString("rasa");
                int hargaBarang = rs.getInt("Harga");
                int qty = rs.getInt("kuantitas");
                if (jenisBarang.equalsIgnoreCase("Etc")) {
                    war.addBarangLain(jenis, idBarang, namaBarang, jenisBarang, qty, hargaBarang);
                } else {
                    war.addMakanMinum(rasa, jenis, idBarang, namaBarang, jenisBarang, qty, hargaBarang);
                }
                df.addRow(new Object[]{i, namaBarang, jenisBarang, hargaBarang, qty});
                i++;
            }

        } catch (SQLException ex) {
            Logger.getLogger(HalPelanggan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
        public final void sortTableBarang() {
        int click = 1;
        TableRowSorter<TableModel> sorter = new TableRowSorter<>(tableBarang.getModel());
        tableBarang.setRowSorter(sorter);
        ArrayList<RowSorter.SortKey> sortKeys = new ArrayList<>();

        int columnIndexToSort = 1;
        if (click % 2 == 0) {
            sortKeys.add(new RowSorter.SortKey(columnIndexToSort, SortOrder.ASCENDING));
        } else {
            sortKeys.add(new RowSorter.SortKey(columnIndexToSort, SortOrder.DESCENDING));
        }
        click++;

        sorter.setSortKeys(sortKeys);
        sorter.sort();
    }

